--[[
    FiveDivision :: Defend the cargo.
    This resource will add "defend" missions to FiveDivision.

    Players can join this mission as Solo or Groups. Players will have to defend area from enemies.
    Once every 5 rounds, elite enemies will spawn, with Armor, increased health and no headshots.

    The more 5-round sets you complete, the more enemies, and more strenght will appear.
    After a couple of rounds, most enemies will become bullet sponges.

    If enemies are within the defend zone for too long, you will lose the mission, and receive the rewards for the rounds you completed.

